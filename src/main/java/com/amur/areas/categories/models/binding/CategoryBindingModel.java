package com.amur.areas.categories.models.binding;


public class CategoryBindingModel {

    private long id;

    private String name;

    public CategoryBindingModel() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
